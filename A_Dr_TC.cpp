void Chal_Ja_Plz(){
   int n;cin>>n;
   
   int ans=0;
   string s;cin>>s;
   int t=count(s.begin(),s.end(),'1');
   for(int i=0;i<n;i++){
    if(s[i]=='1')ans+=t-1;
    else ans+=t+1;
   }
   cout<<ans<<"\n";
}
 
