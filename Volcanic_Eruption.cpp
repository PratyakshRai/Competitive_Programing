template <typename T> void _print(const T &x) {
    cerr << '{'; bool first = true;
    for (const auto &i : x) {
        if (!first) cerr << ", ";
        _print(i); first = false;
    }
    cerr << "}\n";
}

// ------------------- Solve Function -------------
void solve() {
    ll n,p ;
    cin>>n>>p;
    vector<ll>a(n);
    for(ll i =0;i<n;i++)cin>>a[i];
    vector<ll>l(n),r(n);
    ll h=INF;
    for(ll i=0;i<n;i++){
      if(a[i]==0){
        h=0;
        l[i]=0;
      }
      if(a[i]<h){
        l[i]=h;
      }
      else {
        l[i]=a[i];
        h=a[i];
      }
    }
    for(ll i=n-1;i>=0;i--){
      if(a[i]==0){
        h=0;
        r[i]=0;

      }
      if(a[i]>h){
        h=a[i];
        r[i]=h;
      }
      else {
        r[i]=h;
      }
    }

for(ll i=0;i<n;i++){
ll ok =min(l[i],r[i]);
cout<<(ok+p-1)/p<<" ";
}
    cout<<"\n";

}

